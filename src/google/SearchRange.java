    public static ArrayList<Integer> searchRange(final List<Integer> A, int B) {
        ArrayList<Integer> result = new ArrayList<>();
        int left = binarySearch(A,B,true);
        int right = binarySearch(A,B, false);
        result.add(left);
        result.add(right);
        return result;
    }

    public static int binarySearch(List<Integer> list, int num, boolean leftBound){
        int left = 0;int right = list.size()-1;
        int index = -1;
        while(left<=right){
            int mid = (left+right)/2;
            if(list.get(mid)>num){ // 9 > 8, go left
                right = mid-1;
            }
            else if(list.get(mid)<num){ // 7 < 8, go right
                left = mid+1;
            }
            else{ // 8 = 8
                index = mid;
                if(leftBound){ // move left
                    right = mid-1;
                }
                else{
                    left = mid+1;
                }
            }
        }
        return index;
    }
